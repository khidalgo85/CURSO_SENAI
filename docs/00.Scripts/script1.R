##### Basicão #####

#### 1. O R pode ser uma calculadora ####

2 + 3

10/5 # Pode ter ou não espaços entre os elementos

8 * 9

#### 1.1. Você pode salvar tudo em variáveis ou objetos ####

a <- 2 # Agora tem um elemento chamado 'a' que recebeu o valor 2 (deve aparecer na janela Environment)

b = 3 # O símbolo de atribuição no R pode ser o '=' ou '<-', prefira o segundo.

# Você pode usar agora as variáveis para fazer cálculos

a + b

a / b

a^2 # Para exponenciação se usa o acento circunflexo ^

# Os resultados dos cálculos também podem ser salvados em outras variáveis

c <- a^2 + b^a

d <- 3*(sqrt(a*b)) # A função sqrt() é usada para calcular a raíz quadrada

#### 1.2. 'Chamando' as variáveis você consegue obter os valores que receberam ####

c

print(d)

cat('A variável c vale ', c) # A função cat() concatena e printa objetos no console.

#### 1.3. Além de valores númericos as variáveis também podem receber outro tipo de valores, como por exemplo strings ou texto ####

ola <- 'Olá Mundo!'

print(ola)

